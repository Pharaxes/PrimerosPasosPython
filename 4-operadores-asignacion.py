print("#### OPERADORES DE ASIGNACION ####")

#el signo igual asigna el valor a la derecha a la variable
a = 10
b = 3
print(f"Operador =: {a}")

#+= es un operador de caumulacion. Suma el valor que ponemos a la variable
a += b 
print(f"Operador +=: {a}")

a -= b 
print(f"Operador -=: {a}")

a *= b 
print(f"Operador *=: {a}")

a /= b 
print(f"Operador /=: {a}")

a **= b 
print(f"Operador **=: {a}")

cadena = "Hola "
cadena *= 3
#cadena = cadena * 3, es igual a lo anterior 
print(f"Operador *=: {cadena}")